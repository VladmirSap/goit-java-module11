package task1;

public class UserNames {
    private String name;

    public UserNames() {
    }

    public UserNames(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return name;
    }
}
